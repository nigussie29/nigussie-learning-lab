export function getAlgebraOneModule(moduleNumber) {
  return (
    algebraOneModules.find(
      (module) =>
        module.moduleNumber === Number(moduleNumber)
    ) || null
  );
}

export function getAvailableAlgebraOneModules() {
  return algebraOneModules.filter(
    (module) => module.status === "Available"
  );
}

export function getPlannedAlgebraOneModules() {
  return algebraOneModules.filter(
    (module) => module.status === "Planned"
  );
}

